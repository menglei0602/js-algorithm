/**
 * 多指针法——链表的反转
 * 
输入: 1->2->3->4->5->NULL
输出: 5->4->3->2->1->NULL
 */

const {arrayToLinkedList} = require('../common/arrayToLinkedList');
/**
 * @param {ListNode} head
 * @return {ListNode}
 */
const reverseList = function(head) {
    // 初始化前驱结点为 null
    let pre = null;
    // 初始化目标结点为头结点
    let cur = head;
    // 只要目标结点不为 null，遍历就得继续
    while (cur !== null) {
        // 记录一下 next 结点
        let next = cur.next;
        // 反转指针
        cur.next = pre;
        // pre 往前走一步
        pre = cur;
        // cur往前走一步
        cur = next;
    }
    // 反转结束后，pre 就会变成新链表的头结点
    return pre
};

let arr = [1, 2, 3, 4, 5, 6, 7, 8];
let linkListHead = arrayToLinkedList(arr);
console.log(linkListHead);

let newList = reverseList(linkListHead);

console.log('newList:', newList);